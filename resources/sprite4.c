struct fb_t {
unsigned short volatile  pixels[256][512];
};

struct fb_t *const fbp = ((struct fb_t *) 0x8000000);

// assumes 16x16 sprite
void
sprite_draw(struct fb_t *const fbp, unsigned short sprite[], int x, int y) {
 int sxi, syi;
 int xi, yi;
	
	for (sxi = 0; sxi < 16; sxi++)
		for (syi = 0; syi < 16; syi++) {
		   xi = x + sxi;
		   yi = y + syi;
     	   fbp->pixels[yi][xi] = sprite[syi*16+sxi];
	}
}

int xn = 320;
int yn = 240;

unsigned short *ghost[3] = {ghost1, ghost2, ghost3};
unsigned short *bat[3] = {bat1, bat2, bat3};
unsigned short *frog[3] = {frog1, frog2, frog3};
unsigned short *skeleton[4] = {skeleton1, skeleton2, skeleton3, skeleton4};

void
sprite_scroll(struct fb_t *const fbp) {
	int x, y, s, ss;

    s = 0; ss = 0;
	for (x = 0; x < xn; x+=1) {
       sprite_draw(fbp, ghost[s], x, 120);
       sprite_draw(fbp, bat[s], xn - 16 - x, 20);
       sprite_draw(fbp, frog[s], x, 60);
       sprite_draw(fbp, skeleton[ss], xn - 16 -x, 80);
		

	   s = (s + 1) % 2;
	   ss = (ss + 1) % 3;
	waitasec(2);
	
	}
  

}

void
solid_color(struct fb_t *const fbp, unsigned short color) {
    int x, y;
	for (x = 0; x < xn; x++)
       for (y = 0; y < yn; y++) 
          fbp->pixels[y][x] = color; // set pixel value at x,y
}


