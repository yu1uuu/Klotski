void draw_box(struct fb_t *const fbp, unsigned short box[], int x, int y) {
 int sxi, syi;
 int xi, yi;
	
	for (sxi = 0; sxi < 16; sxi++)
		for (syi = 0; syi < 16; syi++) {
		   xi = x + sxi;
		   yi = y + syi;
     	   fbp->pixels[yi][xi] = box[syi*16+sxi];
	}
}

